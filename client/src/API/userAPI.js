import { BASE_URL } from "../config";
const API = BASE_URL 

export const getUsers = ({ token }) => {
    
    return fetch(`/auth/users`, {
        method: "GET",
        headers: {
            Accept: "application/json",
            "Content-Type": "application/json",
            Authorization: `Bearer ${token}`
        }
    })
        .then(response => {
            return response.json();
        })
        .catch(err => {
            console.log(err);
        });
};

